# ধাপ ২: A[3] এর মানকে ৪ দিয়ে গুণ করে নতুন অফসেট বের করা
sll  $t1, $t0, 2        # $t1 = $t0 * 4 (Shift Left Logical দ্বারা ৪ দিয়ে গুণ করা হলো)

# ধাপ ৩: বেস অ্যাড্রেসের সাথে নতুন অফসেট যোগ করে ঠিকানায় পৌঁছানো
add  $t1, $t1, $s2       # $t1 = A এর বেস অ্যাড্রেস + নতুন অফসেট (অর্থাৎ A[A[3]] এর মেমরি অ্যাড্রেস)

# ধাপ ৪: ফাইনাল মানটি মেমরি থেকে লোড করা
